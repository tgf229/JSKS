package com.jsksy.app.bean.zz;

public class PointDoc
{
    private String KMId;
    
    private String KMName;
    
    private String KMLevel;
    
    private String cheatType;

    public String getKMId()
    {
        return KMId;
    }

    public void setKMId(String kMId)
    {
        KMId = kMId;
    }

    public String getKMName()
    {
        return KMName;
    }

    public void setKMName(String kMName)
    {
        KMName = kMName;
    }

    public String getKMLevel()
    {
        return KMLevel;
    }

    public void setKMLevel(String kMLevel)
    {
        KMLevel = kMLevel;
    }

    public String getCheatType()
    {
        return cheatType;
    }

    public void setCheatType(String cheatType)
    {
        this.cheatType = cheatType;
    }
}
